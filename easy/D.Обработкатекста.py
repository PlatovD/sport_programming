def my_ord(ch):
    if ch == '.':
        return 26
    if ch == ',':
        return 27
    if ch == '?':
        return 28
    if ch == '!':
        return 29
    if ch == ' ':
        return 30
    return ord(ch) - ord('a')


s = input()

res0 = ''
res1 = ''
res2 = ''
for ch in s:
    res0 += symbols[0][my_ord(ch)]
    res1 += symbols[1][my_ord(ch)]
    res2 += symbols[2][my_ord(ch)]
print(res0)
print(res1)
